package ch03_for;


public class ForExam05Gugu01 {

	public static void main(String[] args) {
		
			  //for문 전형적인 이중반복 코드  
	    for(int i=1; i<=9; i++) {
	    	for(int j=1; j<=9; j++) {
	    	
	    System.out.print(j+ "*" + i + "=" + i*j + "\t");
	    	}
	    	System.out.println(); // 한줄 밑으로 내리는 명령문;
	    }

	}

}
