package ch01;

public class Operator01Exam {

	public static void main(String[] args) {


		/* 단항 연산자 */
		
		int x = -100;  // - 는 음수
		System.out.println(x);
		int restul = +x;  // 양수 +(-) => -
		System.out.println(restul);
		int restul2 = -x; //  -x;-(-) => +
		System.out.println(restul2);

	}

}
